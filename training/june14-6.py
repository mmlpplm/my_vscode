# 获取最大值函数

# 最简单的
print(max(1,2))
print(max('a','b'))

# 也可以对列表list和元组tuple使用
print(max([5,6]))
print(max((9,7)))

#等多
print('80,100,1000 最大值为：',max(80,100,1000))
print('-20,100,400 最大值为：',max(-20,100,400))
print('-80,-20,-10 最大值为：',max(-80,-20,-10))
print('0,100,-400 最大值为：',max(0,100,-400))


